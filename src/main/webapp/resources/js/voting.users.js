$(function () {
    makeEditable({
        "columns": [
            {
                "data": "name"
            },
            {
                "data": "email",
                "render": function (data, type, row) {
                    if (type === "display") {
                        return "<a href='mailto:" + data + "'>" + data + "</a>";
                    }
                    return data;
                }
            },
            {
                "data": "roles",
                "render": function (data) {
                    let row = '';
                    $.each(data, function (key, value) {
                        if (key === (data.length - 1)) {
                            row += value;
                        } else {
                            row = row + value + ', ';
                        }
                    });
                    return row;
                }
            },
            {
                "orderable": false,
                "defaultContent": "",
                "render": renderEditBtn
            },
            {
                "orderable": false,
                "defaultContent": "",
                "render": renderDeleteBtn
            }
        ],
        "order": [
            [
                0,
                "asc"
            ]
        ]
    });
});